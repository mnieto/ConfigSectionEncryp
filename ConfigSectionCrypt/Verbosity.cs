/// <summary>
/// Logging verbosity level
/// </summary>
public enum Verbosity {
    /// <summary>
    /// Only unexpected errors are shown
    /// </summary>
    Quiet = 4,

    /// <summary>
    /// Normal logging
    /// </summary>
    Normal = 2,

    /// <summary>
    /// Verbose logging
    /// </summary>
    Debug = 1
}
